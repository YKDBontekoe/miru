class AgentCreate(BaseModel):
    """Input model for creating an agent."""

    name: str = Field(min_length=1, max_length=100)
    personality: str = Field(min_length=1, max_length=1000)
    description: str | None = Field(default=None, max_length=500)
    goals: list[str] = Field(default_factory=list)
    capabilities: list[str] = Field(default_factory=list)
    integrations: list[str] = Field(default_factory=list)
    system_prompt: str | None = None


class AgentGenerate(BaseModel):
    """Input for AI-powered agent generation."""

    keywords: str = Field(min_length=1)


class AgentGenerationResponse(BaseModel):
    """Full agent profile generated by AI."""

    name: str
    personality: str
    description: str
    goals: list[str]
    capabilities: list[str]
    suggested_integrations: list[str]


