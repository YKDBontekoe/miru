async def create_agent(agent_data: AgentCreate, user_id: UUID) -> AgentResponse:
    """Create a new agent with full profile."""
    supabase = get_supabase()

    # Build system prompt if not explicitly provided
    system_prompt = agent_data.system_prompt or build_system_prompt(
        name=agent_data.name,
        personality=agent_data.personality,
        description=agent_data.description,
        goals=agent_data.goals,
        capabilities=agent_data.capabilities,
    )

    insert_data: dict[str, Any] = {
        "user_id": str(user_id),
        "name": agent_data.name,
        "personality": agent_data.personality,
        "description": agent_data.description,
        "goals": agent_data.goals,
        "capabilities": agent_data.capabilities,
        "integrations": agent_data.integrations,
        "system_prompt": system_prompt,
    }

    response = supabase.table("agents").insert(insert_data).execute()
    data = cast("list[dict[str, Any]]", response.data)[0]
    return _agent_from_row(data)


async def get_agents(user_id: UUID) -> list[AgentResponse]:
    """List all agents for a user."""
    supabase = get_supabase()
    response = supabase.table("agents").select("*").eq("user_id", str(user_id)).execute()
    return [_agent_from_row(record) for record in cast("list[dict[str, Any]]", response.data)]


def _agent_from_row(data: dict[str, Any]) -> AgentResponse:
    """Convert a database row to an AgentResponse, handling JSONB fields."""
    goals = data.get("goals", [])
    if isinstance(goals, str):
        goals = json.loads(goals)

    capabilities = data.get("capabilities", [])
    if isinstance(capabilities, str):
        capabilities = json.loads(capabilities)

    integrations = data.get("integrations", [])
    if isinstance(integrations, str):
        integrations = json.loads(integrations)

    return AgentResponse(
        id=str(data["id"]),
        name=data["name"],
        personality=data["personality"],
        description=data.get("description"),
        goals=goals if goals else [],
        capabilities=capabilities if capabilities else [],
        integrations=integrations if integrations else [],
        system_prompt=data.get("system_prompt"),
        status=data.get("status", "active"),
        created_at=str(data["created_at"]),
    )


async def generate_agent(keywords: str) -> AgentGenerationResponse:
    """Generate a full agent profile from keywords using AI.

    Returns a rich profile including name, personality, description,
    goals, capabilities, and suggested integrations — all inferred
    from the user's keywords.
    """
    available_caps = [c["id"] for c in AVAILABLE_CAPABILITIES]
    available_integrations = [i["type"] for i in AVAILABLE_INTEGRATIONS]

    prompt = (
        f"Create a creative and unique AI agent/persona based on these keywords: {keywords}.\n\n"
        "Respond with a JSON object containing:\n"
        '- "name": A creative, memorable name for this agent\n'
        '- "personality": A concise system prompt (2-4 sentences) defining how '
        "this agent behaves, speaks, and thinks\n"
        '- "description": A one-sentence summary of what this agent does\n'
        '- "goals": An array of 2-4 specific goals this agent works towards '
        "(e.g. help user stay organized, provide creative inspiration)\n"
        '- "capabilities": An array of relevant capability IDs from this list: '
        f"{available_caps}\n"
        '- "suggested_integrations": An array of relevant integration types '
        f"from this list: {available_integrations}\n\n"
        "Only include capabilities and integrations that genuinely match the "
        "agent's purpose. Be creative with the name and personality.\n\n"
        "Example JSON:\n"
        "{\n"
        '  "name": "Maestro",\n'
        '  "personality": "You are a passionate music curator and DJ. You speak '
        "with enthusiasm about rhythms and melodies. You help users discover new "
        'music and create the perfect playlists for any mood.",\n'
        '  "description": "A music-obsessed curator who helps you discover and '
        'organize your perfect soundtrack",\n'
        '  "goals": ["Help discover new music matching user taste", '
        '"Create mood-based playlists", "Track listening habits and suggest '
        'improvements"],\n'
        '  "capabilities": ["web_search", "memory_recall"],\n'
        '  "suggested_integrations": ["spotify"]\n'
        "}"
    )

    messages = [
        {
            "role": "system",
            "content": (
                "You are a creative AI agent designer. You generate detailed, "
                "well-thought-out agent profiles as JSON. Always respond with "
                "valid JSON only — no markdown fences, no explanation."
            ),
        },
        {"role": "user", "content": prompt},
    ]

    response_text = await chat_completion(messages)

    cleaned_json = response_text.strip()
    if cleaned_json.startswith("```json"):
        cleaned_json = cleaned_json[7:]
    if cleaned_json.startswith("```"):
        cleaned_json = cleaned_json[3:]
    if cleaned_json.endswith("```"):
        cleaned_json = cleaned_json[:-3]
    cleaned_json = cleaned_json.strip()

    data = json.loads(cleaned_json)

    # Validate capabilities and integrations against known lists
    valid_caps = {c["id"] for c in AVAILABLE_CAPABILITIES}
    valid_integrations = {i["type"] for i in AVAILABLE_INTEGRATIONS}

    return AgentGenerationResponse(
        name=data["name"],
        personality=data["personality"],
        description=data.get("description", ""),
        goals=data.get("goals", []),
        capabilities=[c for c in data.get("capabilities", []) if c in valid_caps],
        suggested_integrations=[
            i for i in data.get("suggested_integrations", []) if i in valid_integrations
        ],
    )


